export default class Book {
    constructor(id, title, author, image, releaseDate, shortBio, longBio, discount, price) {
        this.id = id
        this.title = title
        this.author = author
        this.image = image
        this.releaseDate = releaseDate
        this.shortBio = shortBio
        this.longBio = longBio
        this.discount = discount
        this.price = price
    }
}